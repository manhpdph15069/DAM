    private void fillTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblNguoiHoc.getModel();
        dtm.setRowCount(0);
        try {
            String keyword = txtTimKiem.getText();
            List<NguoiHoc> list = dao.selectByKeyword(keyword);
            for (NguoiHoc n : list) {
                Object[] row = {
                    n.getMaNH(),
                    n.getHoTen(),
                    n.isGioiTinh() ? "Nam" : "Nữ",
                    XDate.toString(n.getNgaySinh(), "dd/MM/yyyy"),
                    n.getDiemThoai(),
                    n.getEmail(),
                    n.getMaNV(),
                    XDate.toString(n.getNgayDK(), "dd/MM/yyyy")
                };
                dtm.addRow(row);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn");
            e.printStackTrace();
        }
    }

